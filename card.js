const parentContainer = document.querySelector('.mein-menu');

class MainMenu {
    constructor(title, images, href, parent) {
        this.title = title;
        this.images = images;
        this.parent = parent;
        this.href = href;
    }

    render() {
        const element = document.createElement('article');
        element.classList.add('list-item' , 'main-list');

        const slides = this.images.map(imgSrc => 
            `<a href=${this.href} class="swiper-slide"><img src="${imgSrc}" alt="${this.title}" /></a>`
        ).join('');

        element.innerHTML = `
            <div class="swiper">
                <div class="swiper-wrapper">
                    ${slides}
                </div>
                <div class="swiper-pagination"></div>
                <div class="swiper-button-prev"></div>
                <div class="swiper-button-next"></div>
            </div>
            <h3 class="oswald-medium">${this.title}</h3>
            <a href=${this.href} class="view-more-btn montserrat-light">more</a>
        `;

        this.parent.append(element);

        new Swiper(element.querySelector('.swiper'), {
            direction: 'horizontal',
            loop: true,
            pagination: {
                el: '.swiper-pagination',
            },
            navigation: {
                nextEl: '.swiper-button-next',
                prevEl: '.swiper-button-prev',
            },
        });
    }
}

balenciagaMainMenu.forEach(item => {
    new MainMenu(item.title, item.images, item.href, parentContainer).render();
});